namespace FogOfWar
{
    /// <summary>
    /// Состояния видимости клетки в тумане войны
    /// </summary>
    public enum FogOfWarState
    {
        Hidden,      // Невидимая (полностью скрыта туманом)
        Explored,    // Исследованная (была видна ранее, но сейчас не видна - затемненная)
        Visible      // Видимая (сейчас видна юнитам)
    }
}



